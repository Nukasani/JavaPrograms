
public class Myclass 
{
	public static void main(String[] args) 
	{
		
//    * * * * *       
//      * * * *
//        * * *
//          * *
//            *
		int i,j,k;
		
		for(i=1;i<=5;i++) // number of rows 
		{
			for(j=1;j<=i;j++) //spaces
			{
				System.out.print(" ");
			}
				for(k=5;k>=i;k--)// stars 
				{
					System.out.print("*");
				}
				System.out.println();
			}
		

			
		}
    
	}

	
